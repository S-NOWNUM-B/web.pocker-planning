// createRoomId — это функция, которая генерирует идентификатор комнаты на основе ее названия. Она принимает строку roomName, удаляет лишние пробелы, приводит все символы к нижнему регистру, заменяет все неалфавитно-цифровые символы на дефисы, удаляет ведущие и завершающие дефисы, обрезает результат до 64 символов и возвращает получившуюся строку. Если после всех преобразований строка оказывается пустой, возвращается значение 'room'. Эта функция используется для создания удобочитаемых и уникальных идентификаторов комнат на основе их названий.
export function createRoomId(roomName: string) {
  return (
    roomName
      .trim()
      .toLowerCase()
      .replace(/[^a-z0-9а-яё]+/gi, '-')
      .replace(/^-+|-+$/g, '')
      .slice(0, 64) || 'room'
  );
}

// getAverageVote — это функция, которая принимает массив игроков и вычисляет среднее значение их голосов. Она фильтрует голоса, исключая null, '?', '☕' и нечисловые значения, затем преобразует оставшиеся голоса в числа и вычисляет их среднее арифметическое. Если нет числовых голосов, функция возвращает '0'. Результат округляется до ближайшего целого числа и возвращается в виде строки. Эта функция используется для отображения средней оценки задачи на основе голосов игроков в игре для планирования покера.
export function getAverageVote(players: Player[]) {
  const numericVotes = players
    .map((player) => player.vote)
    .filter((vote): vote is string => vote !== null && vote !== '?' && vote !== '☕')
    .map(Number)
    .filter((vote) => !Number.isNaN(vote));

  if (numericVotes.length === 0) {
    return '0';
  }

  return Math.round(
    numericVotes.reduce((sum, vote) => sum + vote, 0) / numericVotes.length,
  ).toString();
}
